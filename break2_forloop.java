public class break2_forloop 
{
    public static void main(String a[])
    {
      int number=10;
      do{
        System.out.println(number);    //PRINT OUT THE CURRENT NUMBER
        number++;                // INCREAMENT CURRENT NUMBER BY 1
        if(number==16)break;    //STOP / BREAK THE LOOP WHENEVER number BECOME 16
      } 
      while(number<=20);     //CONDITION TO BE FOLLOWED BY COMPILER, 
                            //IF TRUE IT LOOP AGAIN TILL THE CONDITION BECOME FALSE
    }
    } 
    


